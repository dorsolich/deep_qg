import json
from sklearn.base import BaseEstimator, TransformerMixin
from datasets import load_dataset
from qg.config.config import get_logger, LEARNINGQ_DATA_DIR
_logger = get_logger(logger_name=__file__)

class DatasetLoader(BaseEstimator, TransformerMixin):
    
    def __init__(self, dataset, split, data_dir=None):
        self.dataset = dataset
        self.split = split
        self.data_dir = data_dir

    def fit(self, X, y=None):
        return self

    def transform(self, X: dict) -> dict:

        # dataset for training and validating the classifier
        if self.dataset == "LearningQ":
            with open(LEARNINGQ_DATA_DIR/"cls_balanced_dataset.json") as f:
                dataset = json.load(f)
            X["dataset"] = dataset[self.split]
            _logger.info(f"{self.split} set of length {len(X['dataset']['text'])} correctly loaded to the pipeline")

        # dataset for training and validating the question generation models
        elif self.dataset == "squad_v2":
            X["dataset"] = load_dataset('squad_v2', split=self.split)
            _logger.info(f"{self.split} set of length {len(X['dataset'])} correctly loaded to the pipeline")

        # dataset for classifying any list of questions (the questions generated by the QG models)
        elif self.dataset == "generated_questions":
            with open(self.data_dir, encoding="utf-8") as f:
                data = json.load(f)
                questions = data["predictions"]
            X["dataset"] = questions
            _logger.info(f"{len(X['dataset'])} questions to be classified correctly loaded to the pipeline")

        else:
            raise ValueError('Select dataset="squad_v2" or "LearningQ" and split="train" or "validation" or select "generated_questions"')
            
        return X